function buildPriceBaseline() {
  return ingredientSpecs.map(([name, category], index) => {
    const base = category === "水产" ? 32 : category === "肉蛋" ? 18 : category === "豆制品" ? 8 : category === "蔬菜" ? 6 : 10;
    const normal = Number((base + (index % 5) * 1.2).toFixed(1));
    const current = priceOverrides[name] ?? Number((normal * (index % 7 === 0 ? 1.32 : index % 5 === 0 ? 0.78 : 1)).toFixed(1));
    const expensiveThreshold = priceOverrides[name]
      ? Number((current * 0.92).toFixed(1))
      : Number((normal * 1.25).toFixed(1));
    return {
      name,
      category,
      unit: "斤",
      normal_price: normal,
      cheap_threshold: Number((normal * 0.82).toFixed(1)),
      expensive_threshold: expensiveThreshold,
      current_price: current,
      source_note: priceOverrides[name]
        ? "参考农业农村部/价格监测公开数据后人工设定的深圳家庭买菜贵价基线"
        : "基线占位：后续接农业农村部主要菜篮子价格日动态"
    };
  });
}

